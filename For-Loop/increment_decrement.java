package For_loop;

public class increment_decrement {

	public static void main(String[] args) {
		int a = 10;
		
		System.out.println("a: " + a);
		System.out.println("Post increment: " + a++);
		System.out.println("a after post increment: " + a);
		
		int b = 5;
		System.out.println("b: " + b);
		System.out.println("Pre increment: " + (++b));
		System.out.println("b after pre increment: " + b);
		
		int c= 15;
		
		System.out.println("c: " + c);
		System.out.println("Post decrement: " + c--);
		System.out.println("c after post decrement: " + c);
		
		int d = 20;
		
		System.out.println("d: " + d);
		System.out.println("Pre decrement: " + (--d));
		System.out.println("d after pre decrement: " + d);
		
	}

}
